void calculateDFT(float *signal, int tamano_ventana, float *realOut, float *imagOut)
{
    for (int k = 0; k < tamano_ventana; k++)
    {
        realOut[k] = 0;
        imagOut[k] = 0;
        for (int n = 0; n < tamano_ventana; n++)
        {
            float angle = 2 * M_PI * k * n / tamano_ventana;
            realOut[k] += signal[n] * cos(angle);
            imagOut[k] -= signal[n] * sin(angle);
        }
    }
}

float calculateAverageMagnitude(float *real, float *imag, int tamano_ventana)
{
    float sum_magnitudes = 0;
    for (int k = 0; k < tamano_ventana; k++)
    {
        sum_magnitudes += sqrt(real[k] * real[k] + imag[k] * imag[k]);
    }
    return sum_magnitudes / tamano_ventana; // Amplitud promedio en la ventana
}

void calculateAverageAmplitudePerWindow(float *signal, int tamano_ventana, int superposicion, float *amplitudes_promedio, float *indices_tiempo)
{
    int num_ventanas = N / tamano_ventana;
    float realOut[VENTANA];
    float imagOut[VENTANA];

    for (int i = 0; i < num_ventanas; i++)
    {
        // Definir la ventana actual
        int inicio = i * (tamano_ventana - superposicion);
        float ventana[VENTANA];

        // Copiar los datos de la señal a la ventana
        for (int j = 0; j < tamano_ventana; j++)
        {
            ventana[j] = signal[inicio + j];
        }

        // Calcular la DFT de la ventana actual
        calculateDFT(ventana, tamano_ventana, realOut, imagOut);

        // Calcular la amplitud promedio de la ventana actual
        amplitudes_promedio[i] = calculateAverageMagnitude(realOut, imagOut, tamano_ventana);

        // Calcular el índice de tiempo en segundos para la ventana actual
        indices_tiempo[i] = (float)(inicio + tamano_ventana / 2) / FS; // Tiempo del centro de la ventana
    }

    flag = 1;
}

